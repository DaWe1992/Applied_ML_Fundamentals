import numpy as np


# -----------------------------------------------------------------------------
# Functions
# -----------------------------------------------------------------------------

def kullback_leibler_div(P, Q):
    """
    Computes the Kullback-Leibler divergence.
    
    :param P:       distribution 1
    :param Q:       distribution 2
    """
    return np.sum(np.multiply(P, np.log(P / Q)))
    
    
