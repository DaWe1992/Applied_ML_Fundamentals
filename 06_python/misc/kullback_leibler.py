if __name__ == "__main__":    
    P = np.asarray([0.10, 0.24, 0.30, 0.16, 0.20])
    Q = np.asarray([0.16, 0.10, 0.24, 0.20, 0.30])
    
    print(kullback_leibler_div(P, Q))
    